my_tuples = (10, 20, 30, 40, 50)
print(my_tuples)

print("\nAccessing by index:")
# Accessing by index
print(my_tuples[0])
print(my_tuples[-1])

print("\nLooping through List:")
# Looping through list
for num in my_tuples:
    print(num)

print("\nSlicing tuple:")
# Slicing tuple
print(my_tuples[1:4])